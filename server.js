// =======================================
// <<<<<<<<<<<<DEPENDENCIES>>>>>>>>>>>>>>>
// =======================================
const express = require('express')
const app = express();
const port = 3000;


// =======================================
// <<<<<<<<<<<<MIDDLEWARE>>>>>>>>>>>>>>
// =======================================




// =======================================
// <<<<<<<<<<<<INDEX ROUTE>>>>>>>>>>>>>>>
// =======================================
app.get('/',(req,res) => {
  res.render('index.ejs')
})

// =======================================
// <<<<<<<<<<<<SHOW ROUTE>>>>>>>>>>>>>>>
// =======================================
app.get('/:id',(req,res) => {
  res.render('show.ejs')
})


// =======================================
// <<<<<<<<<<<<LISTEN>>>>>>>>>>>>>>>
// =======================================
app.listen(port,() => {
  console.log('listening to port:',port);
})
